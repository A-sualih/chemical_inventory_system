const PDFDocument = require('pdfkit');
const Chemical = require('../../models/Chemical');

exports.getSafetyDashboard = async (req, res) => {
  try {
    const labQuery = (req.user.role === 'Admin' && !req.activeLabId) ? {} : { lab: req.activeLabId };
    
    const total = await Chemical.countDocuments({ ...labQuery, archived: false });
    const restricted = await Chemical.countDocuments({ ...labQuery, restricted_access: true, archived: false });
    const needsTraining = await Chemical.countDocuments({ ...labQuery, training_required: true, archived: false });
    const sdsPending = await Chemical.countDocuments({ 
        ...labQuery,
        $or: [
            { sds_attached: false },
            { "sds_docs.verification_status": "Pending" }
        ],
        archived: false 
    });
    
    const hazards = await Chemical.aggregate([
      { $match: { ...labQuery, archived: false } },
      { $unwind: { path: "$ghs_classes", preserveNullAndEmptyArrays: false } },
      { $group: { _id: "$ghs_classes", count: { $sum: 1 } } }
    ]);

    const risks = await Chemical.aggregate([
      { $match: { ...labQuery, archived: false } },
      { $group: { _id: "$exposure_details.risk_level", count: { $sum: 1 } } }
    ]);

    res.json({
      summary: {
        total,
        restricted,
        needsTraining,
        sdsPending
      },
      hazards,
      risks
    });
  } catch (err) {
    res.status(500).json({ error: 'Failed to fetch safety dashboard data' });
  }
};

exports.checkIncompatibility = async (req, res) => {
  try {
    const { location } = req.params;
    const labQuery = (req.user.role === 'Admin' && !req.activeLabId) ? {} : { lab: req.activeLabId };
    const chemicals = await Chemical.find({ ...labQuery, location, archived: false });
    
    if (chemicals.length < 2) return res.json({ incompatible: false, conflicts: [] });
    
    const conflicts = [];
    const incompatiblePairs = [
      ['Acid', 'Base'],
      ['Flammable', 'Oxidizer'],
      ['Acid', 'Oxidizer'],
      ['Base', 'Oxidizer'],
      ['Water-Reactive', 'Moisture']
    ];

    for (let i = 0; i < chemicals.length; i++) {
      for (let j = i + 1; j < chemicals.length; j++) {
        const c1 = chemicals[i];
        const c2 = chemicals[j];
        
        for (let pair of incompatiblePairs) {
          if (pair.includes(c1.chemical_family) && pair.includes(c2.chemical_family) && c1.chemical_family !== c2.chemical_family) {
            conflicts.push({
              chemicals: [c1.name, c2.name],
              reason: `Incompatible families: ${c1.chemical_family} and ${c2.chemical_family}`,
              severity: 'High'
            });
          }
        }
        
        if (c1.incompatibility?.includes(c2.name) || c1.incompatibility?.includes(c2.chemical_family)) {
            conflicts.push({
                chemicals: [c1.name, c2.name],
                reason: `${c1.name} is explicitly incompatible with ${c2.name}/${c2.chemical_family}`,
                severity: 'Critical'
            });
        }
      }
    }
    
    res.json({
      incompatible: conflicts.length > 0,
      conflicts
    });
  } catch (err) {
    res.status(500).json({ error: 'Incompatibility check failed' });
  }
};

